# 대소문자 변환


# 소문자 -> 대문자 : result = text.upper()
text = "hello world"
result = text.upper()
print(result)  # 출력: HELLO WORLD



# 대문자 -> 소문자 : result = text.lower()
text = "HELLO WORLD"
result = text.lower()
print(result)



# 대문자 -> 소문자 + 소문자 -> 대문자 : result = text.swapcase()
text = "Hello World"
result = text.swapcase()
print(result)  # 출력: hELLO wORLD
